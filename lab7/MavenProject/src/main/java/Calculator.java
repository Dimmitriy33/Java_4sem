public class Calculator {
    public static int Sum(int x, int y) {
        return x + y;
    }

    public static int Dif(int x, int y) {
        return x - y;
    }

    public static int Mul(int x, int y) {
        return x * y;
    }

    public static int Div(int x, int y) {
        return x / y;
    }

    public static int Mod(int x, int y) {
        return x % y;
    }
}
